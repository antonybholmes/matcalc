package edu.columbia.rdf.matcalc.toolbox.core;

import org.jebtk.modern.AssetService;
import org.jebtk.modern.event.ModernClickEvent;
import org.jebtk.modern.event.ModernClickListener;
import org.jebtk.modern.graphics.icons.TableVectorIcon;
import org.jebtk.modern.ribbon.RibbonLargeButton;

import edu.columbia.rdf.matcalc.MainMatCalcWindow;
import edu.columbia.rdf.matcalc.toolbox.Module;

/**
 * Split table into multiple tables by grouping column values and then create a
 * zip of the results.
 *
 * @author Antony Holmes
 *
 */
public class SummaryModule extends Module implements ModernClickListener {

  /**
   * The member window.
   */
  private MainMatCalcWindow mWindow;

  /*
   * (non-Javadoc)
   * 
   * @see org.abh.lib.NameProperty#getName()
   */
  @Override
  public String getName() {
    return "Summary";
  }

  /*
   * (non-Javadoc)
   * 
   * @see edu.columbia.rdf.apps.matcalc.modules.Module#init(edu.columbia.rdf.apps.
   * matcalc.MainMatCalcWindow)
   */
  @Override
  public void init(MainMatCalcWindow window) {
    mWindow = window;

    RibbonLargeButton button = new RibbonLargeButton("Summary",
        AssetService.getInstance().loadIcon(TableVectorIcon.class, 24));
    button.setToolTip("Summary", "Display a basic statistical summary of the matrix.");
    window.getRibbon().getToolbar("Data").getSection("Tools").add(button);

    button.addClickListener(this);
  }

  /*
   * (non-Javadoc)
   * 
   * @see org.abh.lib.ui.modern.event.ModernClickListener#clicked(org.abh.lib.ui.
   * modern .event.ModernClickEvent)
   */
  @Override
  public final void clicked(ModernClickEvent e) {
    SummaryDialog dialog = new SummaryDialog(mWindow);

    dialog.setVisible(true);
  }
}
